<script type="text/javascript">
    $(document).ready(function() {
        var action = "search";

        $("#search_name").keyup(function() {
            var search_name = $("#search_name").val();
            if ($("#search_name").val() != '') {
                $.ajax({
                    url: "home/search",
                    method: "POST",
                    data: {
                        action: action,
                        search_name: search_name
                    },
                    success: function(data) {
                        $("#output_search").html(data);
                    }
                });
            } else {
                $("#output_search").html("");
            }
        });
    })
</script>

